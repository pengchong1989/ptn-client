package com.nms.ui.ptn.business.ces.bean;

import com.nms.ui.frame.ViewDataObj;

@SuppressWarnings("serial")
public class CesPortTableBean extends ViewDataObj {

	private String objectname;
	private String porttype;
	private String sitename;
	private String portname;

	public String getObjectname() {
		return objectname;
	}

	public void setObjectname(String objectname) {
		this.objectname = objectname;
	}

	public String getPorttype() {
		return porttype;
	}

	public void setPorttype(String porttype) {
		this.porttype = porttype;
	}

	public String getSitename() {
		return sitename;
	}

	public void setSitename(String sitename) {
		this.sitename = sitename;
	}

	public String getPortname() {
		return portname;
	}

	public void setPortname(String portname) {
		this.portname = portname;
	}

	@SuppressWarnings("unchecked")
	@Override
	public void putObjectProperty() {
		getClientProperties().put("objectname", this.getObjectname());
		getClientProperties().put("porttype", this.getPorttype());
		getClientProperties().put("sitename", this.getSitename());
		getClientProperties().put("portname", this.getPortname());
	}

}
