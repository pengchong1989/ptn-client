	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
		CodeService_MB codeService = null;

		try {

			if (Verification.jtextfieldNull(jTextField1)) {
				jLabel4.setText("Code名称不能为空,或长度不能小于5");
				jButton1.setEnabled(false);
				return;
			}

			jLabel4.setText("");

			jButton1.setEnabled(true);

			if (Verification.jtextfieldNull(jTextField2)) {
				jTextField2.setBorder(new LineBorder(Color.RED));
				DialogBoxUtil.errorDialog(this, ResourceUtil.srcStr(StringKeysTip.TIP_NOT_FULL));
				return;
			}

			if (null != code) {

				codeService = (CodeService_MB) ConstantUtil.serviceFactory.newService_MB(Services.Code);

				code.setCodeName(jTextField1.getText());
				code.setCodeValue(jTextField2.getText());
				code.setCodeENName(jTextField3.getText());
				codeService.saveOrUpdate(code);

				DialogBoxUtil.succeedDialog(this, ResourceUtil.srcStr(StringKeysTip.TIP_SAVE_SUCCEED));
				this.dispose();

				if (this.codePanel != null) {
					codePanel.tableGroupData();
				}

			}
		} catch (Exception e) {
			ExceptionManage.dispose(e,this.getClass());
		} finally {
			UiUtil.closeService_MB(codeService);
		}
	}

