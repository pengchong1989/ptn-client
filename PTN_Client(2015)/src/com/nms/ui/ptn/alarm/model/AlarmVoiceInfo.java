package com.nms.ui.ptn.alarm.model;

import java.io.Serializable;

public class AlarmVoiceInfo implements Serializable {

	private static final long serialVersionUID = -5166155135582634341L;
	
	private int id;
	private int alarmType;//1:紧急告警,2:重要告警,3:次要告警,4:提示告警
	private int alarmContinueTime;//60-300s
	private int alarmSwitch;//0:关,1:开
	private String alarmSoundPath;//告警声音文件路径
	private int alarmColorRGB;//告警颜色代码rgb,紧急/主要/次要/提示=(-1564897)/(-26368)/(-1447651)/(-7368720)
	
	public int getAlarmColorRGB() {
		return alarmColorRGB;
	}
	public void setAlarmColorRGB(int alarmColorRGB) {
		this.alarmColorRGB = alarmColorRGB;
	}
	public String getAlarmSoundPath() {
		return alarmSoundPath;
	}
	public void setAlarmSoundPath(String alarmSoundPath) {
		this.alarmSoundPath = alarmSoundPath;
	}
	public int getAlarmType() {
		return alarmType;
	}
	public void setAlarmType(int alarmType) {
		this.alarmType = alarmType;
	}
	public int getAlarmContinueTime() {
		return alarmContinueTime;
	}
	public void setAlarmContinueTime(int alarmContinueTime) {
		this.alarmContinueTime = alarmContinueTime;
	}
	public int getAlarmSwitch() {
		return alarmSwitch;
	}
	public void setAlarmSwitch(int alarmSwitch) {
		this.alarmSwitch = alarmSwitch;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
}
