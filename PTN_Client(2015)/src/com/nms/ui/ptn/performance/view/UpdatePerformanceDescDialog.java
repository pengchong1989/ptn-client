	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
		if (null != capability) {
			capability.setCapabilitydisp(jTextArea1.getText());

			CapabilityService_MB capabilityService = null;
			try {
				capabilityService = (CapabilityService_MB) ConstantUtil.serviceFactory.newService_MB(Services.Capability);
				capabilityService.saveOrUpdate(capability);
				//添加日志记录
				AddOperateLog.insertOperLog(jButton1, EOperationLogType.PERFORMANCEUPDATE.getValue(), null);
				DialogBoxUtil.succeedDialog(this, ResourceUtil.srcStr(StringKeysTip.TIP_CONFIG_SUCCESS));
				this.dispose();
				performanceDescPanel.getController().refresh();

			} catch (Exception e) {
				ExceptionManage.dispose(e,this.getClass());
			} finally {
				UiUtil.closeService_MB(capabilityService);
			}
		}
	}

