	private void getCapability(int capabilityId) {

		CapabilityService_MB capabilityService = null;
		List<Capability> capabilityList = null;

		try {
			capabilityService = (CapabilityService_MB) ConstantUtil.serviceFactory.newService_MB(Services.Capability);
			capability = new Capability();
			capability.setId(capabilityId);

			capabilityList = capabilityService.select(capability);
			if (capabilityList.size() > 0) {
				capability = capabilityList.get(0);
			} else {
				capability = null;
			}

		} catch (Exception e) {
			ExceptionManage.dispose(e,this.getClass());
		} finally {
			UiUtil.closeService_MB(capabilityService);
			capabilityList = null;
		}
	}

